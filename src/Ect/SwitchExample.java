package Ect;

public class SwitchExample {

	public static void main(String[] args) {
		int n = 1;
		switch(n) {
		case 1:
			System.out.println("simple");
			break;
		case 2:
			System.out.println("java");
		case 3:
			System.out.println("love");
		case 4:
			System.out.println("best");
		default:
			System.out.println("like");
		}

	}

}
